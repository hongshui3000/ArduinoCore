static uint32_t gn_clp300_flags = 0;
static volatile uint32_t* baseAddr = NULL;

static int get_operand_offset(int opsize)
{
    int ret = 0;

    switch ( opsize )
    {
        case PKA_CTRL_BASE_RADIX_BIT_256:
            ret = PKA_OP_256BIT_OFFSET;
            break;

        case PKA_CTRL_BASE_RADIX_BIT_512:
            ret = PKA_OP_512BIT_OFFSET;
            break;

        case PKA_CTRL_BASE_RADIX_BIT_1024:
            ret = PKA_OP_1024BIT_OFFSET;
            break;

        case PKA_CTRL_BASE_RADIX_BIT_2048:
            ret = PKA_OP_2048BIT_OFFSET;
            break;

        case PKA_CTRL_BASE_RADIX_BIT_4096:
            ret = PKA_OP_4096BIT_OFFSET;
            break;

        default:
            ret = CRYPTO_FAIL;
            break;
    }

    return ret;
}

static uint32_t get_base_radix(int size)
{
    if ( size <= 16 )
        return 0;

    if ( size <= 32 )
        return 2;

    if ( size <= 64 )
        return 3;

    if ( size <= 128 )
        return 4;

    if ( size <= 256 )
        return 5;

    if ( size <= 512 )
        return 6;

    return 0;
}

static volatile uint32_t* get_operand_base(uint8_t* op, int size)
{
    int op_index  = 0;
    int opsize = 0;
    volatile uint32_t* operandbase = NULL;

    switch ( op[0] )
    {
        case 'A':
            operandbase = (volatile uint32_t*)(baseAddr+PKA_OPERAND_A_BASE);
            break;

        case 'B':
            operandbase = (volatile uint32_t*)(baseAddr+PKA_OPERAND_B_BASE);
            break;

        case 'C':
            operandbase = (volatile uint32_t*)(baseAddr+PKA_OPERAND_C_BASE);
            break;

        case 'D':
            operandbase = (volatile uint32_t*)(baseAddr+PKA_OPERAND_D_BASE);
            break;

        default:
            return NULL;
    }

    op_index = op[1] - '0';

    opsize = get_base_radix(size);
    if ( !opsize )
        return NULL;

    opsize = get_operand_offset(opsize);
    operandbase = operandbase + ((op_index<<opsize) >> 2);

    return (uint32_t*)operandbase;
}


static int clp300_response_wait(void)
{
    uint32_t status = 0;

    status = baseAddr[PKA_RC];
    if ( status & (1UL<<PKA_RC_BUSY) )
        return CRYPTO_BUSY;

    status = (status >> PKA_RC_REASON) & 0xFF;

    return status;
}

static void* pka_memcpy(void* dst0, const void* src0, int len0)
{
    char* dst = (char*)dst0;
    char* src = (char*)src0;
    void* save = dst0;

    if ( len0 <= 0 )
        return NULL;

    while ( len0-- )
    {
        *dst++ = *src++;
    }

    return save;
}

int get_pka_flags(void)
{
    return (int)gn_clp300_flags;
}


int ESAL_PKA_Flags(uint8_t* name, ePKA_OP op)
{
    int flagbit = 0;
    uint32_t mask = 0;
    uint32_t prev_flags;

    if ( name[0] == 'F' )
    {
        flagbit = PKA_FLAG_F0 + (name[1] - '0');
        return flagbit;
    }

    switch ( name[0] )
    {
        case 'Z':
            flagbit = PKA_FLAG_ZERO;
            break;

        case 'M':
            flagbit = PKA_FLAG_MEMBIT;
            break;

        case 'B':
            flagbit = PKA_FLAG_BORROW;
            break;

        case 'C':
            flagbit = PKA_FLAG_CARRY;
            break;

        default:
            return CRYPTO_INVALID_ARG;
    }

    mask = 1UL << flagbit;
    prev_flags = gn_clp300_flags;

    switch ( op )
    {
        case PKA_OP_NOP:
            gn_clp300_flags = mask;
            break;

        case PKA_OP_SET:
            gn_clp300_flags |= mask;
            break;

        case PKA_OP_CLR:
            gn_clp300_flags &= ~mask;
            break;

        case PKA_OP_XOR:
            gn_clp300_flags ^= mask;
            break;

        default:
            return  CRYPTO_INVALID_ARG;
    }

    return !!(prev_flags && mask);

}

int ESAL_PKA_RegisterIoctl(int reg, uint32_t* val, int mode)
{
    CRYPTO_ASSERT(!val);

    switch ( mode )
    {
        case PKA_READ_REG:
            *val = baseAddr[reg];
            break;

        case PKA_WRITE_REG:
            baseAddr[reg] = *val;
            break;

        default:
            return CRYPTO_INVALID_ARG;
   }

    return CRYPTO_OK;
}

int ESAL_PKA_ReadOperandRegister(uint8_t* opname, void* outstream, uint32_t size)
{
    int dump = 0;
    int dumpsz = 0;
    uint32_t rd_data = 0;
    volatile uint32_t* operandbase = NULL;

    if ( !opname || !outstream )
        return CRYPTO_MEMORY_ERROR;

    dumpsz = size >> 2;

    operandbase = get_operand_base(opname, size);
    if ( !operandbase )
        return CRYPTO_INVALID_KEYSZ;

    for ( dump=0; dump<dumpsz; dump++ )
    {
        rd_data = operandbase[dump];
        pka_memcpy((uint8_t*)outstream+((dumpsz-dump-1)<<2)+(size&3), (uint8_t*)&rd_data, 4);
    }

    if ( size & 3 )
    {
        rd_data = operandbase[dump];
        pka_memcpy((uint8_t*)outstream, (uint8_t*)&rd_data+sizeof(rd_data)-(size&3), size&3);
    }

    return CRYPTO_OK;
}

int ESAL_PKA_ZeroWriteOperandRegister(uint8_t* opname, int size)
{
    int dump = 0;
    int dumpsz = 0;
    uint32_t w_val = 0x0UL;
    volatile uint32_t* operandbase = NULL;

    if ( !opname )
        return CRYPTO_MEMORY_ERROR;

    dumpsz = size >> 2;

    operandbase = get_operand_base(opname,size);
    if ( operandbase == NULL )
        return CRYPTO_INVALID_KEYSZ;

    for ( dump=0; dump<dumpsz; dump++ )
    {
        operandbase[dump] = w_val;
    }

    if ( size & 3 )
    {
        pka_memcpy((uint8_t*)&operandbase[dump]+sizeof(dumpsz), (uint8_t*)&w_val, size&3);
    }

    return CRYPTO_OK;
}

int ESAL_PKA_WriteOperandRegister(uint8_t* opname, void* instream, uint32_t size)
{
    int dump = 0;
    int dumpsz = 0;
    uint32_t w_val;
    volatile uint32_t* operandbase = NULL;

    if ( !opname || !instream )
        return CRYPTO_MEMORY_ERROR;

    dumpsz = size >> 2;

    operandbase = get_operand_base(opname,size);
    if ( operandbase == NULL )
        return CRYPTO_INVALID_KEYSZ;

    for ( dump=0; dump<dumpsz; dump++ )
    {
        pka_memcpy((uint8_t*)&w_val, (uint8_t*)instream+((dumpsz-dump-1)<<2)+(size&3), 4);
        operandbase[dump] = w_val;
    }

    if ( size & 3 )
    {
        pka_memcpy((uint8_t*)&operandbase[dump]+sizeof(dumpsz), (uint8_t*)instream, size&3);
    }

    return CRYPTO_OK;
}

int ESAL_PKA_WriteOperandMultiple(tPKA_CMD_LIST* pcmd, uint32_t len, uint32_t size)
{
    int idx;
    int ret = CRYPTO_OK;

    for ( idx=0; idx<len; idx++ )
    {
        if ( pcmd->reg_val != NULL )
        {
            ret = ESAL_PKA_WriteOperandRegister(pcmd->reg_name, pcmd->reg_val, size);
            if ( ret != CRYPTO_OK )
                return ret;
        }
        pcmd++;
    }

    return CRYPTO_OK;

}

int ESAL_PKA_FirmwareStart(uint32_t entry, uint32_t flags, uint32_t size)
{
    uint32_t  base =  0;
    uint32_t  ctrl;

    base = get_base_radix(size);
    if ( !base )
        return CRYPTO_INVALID_KEYSZ;

    ctrl = base << PKA_CTRL_BASE_RADIX;
    ctrl |= (size & (size -1) ? (size+3)/4 : 0) << PKA_CTRL_PARTIAL_RADIX;
    ctrl |= 1UL << PKA_CTRL_GO;

    baseAddr[PKA_INDEX_I] = 0x0UL;
    baseAddr[PKA_INDEX_J] = 0x0UL;
    baseAddr[PKA_INDEX_K] = 0x0UL;
    baseAddr[PKA_INDEX_L] = 0x0UL;

    baseAddr[PKA_F_STACK] = 0x0UL;
    baseAddr[PKA_FLAGS]   = flags;
    baseAddr[PKA_ENTRY]   = entry;
    baseAddr[PKA_CTRL]    = ctrl;
    
    while ( clp300_response_wait() != 0 );

    do
    {
        ESAL_PKA_RegisterIoctl(PKA_STATUS, &ctrl, PKA_READ_REG);
    } while( !(ctrl & (1<<PKA_STAT_IRQ)) );

    ctrl  = 1<<PKA_STAT_IRQ;
    ESAL_PKA_RegisterIoctl(PKA_STATUS, &ctrl, PKA_WRITE_REG);
    ESAL_PKA_RegisterIoctl(PKA_FLAGS, &gn_clp300_flags, PKA_READ_REG);
    
    return CRYPTO_OK;
}

void memcpy32(uint32_t *dst, void *src, int len){
	int i;
	uint32_t *src32 = (uint32_t *)src;
    if ( len <= 0 )
        return NULL;

	for(i=0; i<len; i+= sizeof(uint32_t)) {
		*dst = *src32;
		dst++; src32++;
	}
}

void memset32(uint32_t *dst, int len){
	int i;
    if ( len <= 0 )
        return NULL;

	for(i=0; i<len; i+= sizeof(uint32_t)) {
		*dst = 0x00;
		dst++;
	}
}

